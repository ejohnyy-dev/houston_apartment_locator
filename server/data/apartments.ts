// Apply deterministic jitter so coordinates are in the neighborhood but not
// at a specific address. Seed is stable per listing id.
function jitter(base: [number, number], id: number): [number, number] {
  const s1 = Math.sin(id * 127.1) * 43758.5453;
  const s2 = Math.sin(id * 311.7) * 43758.5453;
  const j1 = (s1 - Math.floor(s1) - 0.5) * 0.006; // ~330m
  const j2 = (s2 - Math.floor(s2) - 0.5) * 0.006;
  return [base[0] + j1, base[1] + j2];
}

function coord(neighborhood: string, id: number): [number, number] {
  const base = NC[neighborhood] ?? [29.7604, -95.3698];
  return jitter(base, id);
}

